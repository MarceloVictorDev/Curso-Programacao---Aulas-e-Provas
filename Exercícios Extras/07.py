#Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
#A = b * h

base = float(input("Por favor, digite o valor da base do quadrado."))
altura = float(input("Por favor, digite o valor da altura do quadrado."))
area = base * altura
print(f"O valor da área do quadrado é de {area}. O Dobro desse valor é: {area * 2}")


